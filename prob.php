<p>Question 2: What is the probability of getting head when tossing a coin.</p>
<p>Solution:
Sample Space = {H, T}</p>
<p>Number of possible outcomes = 2</p>
<p>Number of favorable outcomes = 1
(because of only one head) </p>
<p>=> Probability of getting head = Number offavorable outcomesTotal number of possible outcomesNumber offavorable outcomesTotal number of possible outcomes
</p>
<p>= 1212</p>
