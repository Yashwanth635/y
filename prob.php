    <p>Question 1: Two dice are rolled once. Calculate the probability that the sum of the numbers on the two dice is 5.</p>
    <p>Solution:
Possible outcomes (Sample Space) = {(1, 1), (1, 2),...............,(1, 6), (2, 1), (2, 2),................,(2, 6), (3, 1), (3, 2),...........,(3, 6), .............,(4, 1), (4, 2),..........,(4, 6), (5, 1), (5,2),...............,(5, 6), (6, 1), (6, 2),......................,(6, 6)}</p>
<p>Total possible outcomes = 36</p>
<p>Number of outcomes of the experiment that are favorable to the event that a sum of two events is 6</p>
<p>=> Favorable outcomes are: (1, 5), (2, 4), (3, 3), (4, 2) and (5, 1)</p>
<p>Number of favorable outcomes = 5</p>
<p>Use, probability formula = Number offavorable outcomesTotal number of possible outcomesNumber offavorable outcomesTotal number of possible outcomes</p>
<p>= 536536</p>
<p>The probability of a sum of 6 is 536536.</p>
