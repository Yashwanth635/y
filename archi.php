    <p>Question 1.Is a computer architecture class useful for a computer science major who wishes to work as a software engineer?</p>
    <p>answer->If you ever have to write performance-critical code, having some hardware knowledge is essential - how multi-level caching works, how branch predictors work, how pipelining works and how branches can potentially give you a big performance hit, how filling up a TLB cache can cause all memory operations to slow down, how doing a SIMD operation across cacheline boundaries can be very slow, how cache coherence protocols can slow down your multi-threaded code if you use certain memory access patterns, etc.</p>
    <p>And obviously, if you ever want to write kernel code, having some hardware knowledge is also essential.</p>
    <p>If you know you will only ever stay in high level software that's not performance-critical (business applications, most web stuff, etc), it's not very important.</p>
