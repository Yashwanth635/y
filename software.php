    <p>Q.What is computer software?</p>
    <p>A. Computer software is a complete package, which includes software program, its documentation and user guide on how to use the software.</p>
    <br>
    <p>Q.Can you differentiate computer software and computer program?</p>
    <p>A. A computer program is piece of programming code which performs a well defined task where as software includes programming code, its documentation and user guide.</p>
    <br>
    <p>Q.What is software engineering?</p>
    <p>A. Software engineering is an engineering branch associated with software system development.</p>
    <br>
    <p>Q.When you know programming, what is the need to learn software engineering concepts?</p>
    <p>A. A person who knows how to build a wall may not be good at building an entire house. Likewise, a person who can write programs may not have knowledge of other concepts of Software Engineering. The software engineering concepts guide programmers on how to assess requirements of end user, design the algorithms before actual coding starts, create programs by coding, testing the code and its documentation.</p>
    <br>
    <p>Q.What is software process or Software Development Life Cycle (SDLC)?</p>
    <p>A.Software Development Life Cycle, or software process is the systematic development of software by following every stage in the development process namely, Requirement Gathering, System Analysis, Design, Coding, Testing, Maintenance and Documentation in that order.</p>
    <br>
    <p>Q.What are SDLC models available?</p>
    <p>A. There are several SDLC models available such as Waterfall Model, Iterative Model, Spiral model, V-model and Big-bang Model etc.</p>
    <br>
    <p>Q.What are various phases of SDLC?</p>
    <p>A. The generic phases of SDLC are: Requirement Gathering, System Analysis and Design, Coding, Testing and implementation. The phases depend upon the model we choose to develop software.</p>
    <br>
    <p>Q.What is software project management?</p>
    <p>A. Software project management is process of managing all activities like time, cost and quality management involved in software development.</p>
    <br>
    <p>Q.Who is software project manager?</p>
    <p>A. A software project manager is a person who undertakes the responsibility of carrying out the software project.</p>
    <br>
    <p>Q.What does software project manager do?</p>
    <p>A. Software project manager is engaged with software management activities. He is responsible for project planning, monitoring the progress, communication among stakeholders, managing risks and resources, smooth execution of development and delivering the project within time, cost and quality contraints.</p>
    <br>
    <p>Q.What is software scope?</p>
    <p>A. Software scope is a well-defined boundary, which encompasses all the activities that are done to develop and deliver the software product.

The software scope clearly defines all functionalities and artifacts to be delivered as a part of the software. The scope identifies what the product will do and what it will not do, what the end product will contain and what it will not contain.</p>
    <br>
    <p>Q.What is project estimation?</p>
    <p>A. It is a process to estimate various aspects of software product in order to calculate the cost of development in terms of efforts, time and resources. This estimation can be derived from past experience, by consulting experts or by using pre-defined formulas.</p>
    <br>
    <p>Q.How can we derive the size of software product?</p>
    <p>A. Size of software product can be calculated using either of two methods -

Counting the lines of delivered code
Counting delivered function points</p>
    <br>
    <p>Q.What are function points?</p>
    <p>A. Function points are the various features provided by the software product. It is considered as a unit of measurement for software size.</p>

    <br>
    <p>Q.What are software project estimation techniques available?</p>
    <p>A. There are many estimation techniques available.The most widely used are -

Decomposition technique (Counting Lines of Code and Function Points)
Empirical technique (Putnam and COCOMO).</p>
